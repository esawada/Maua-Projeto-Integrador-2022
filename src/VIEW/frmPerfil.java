    private void listarValores() {
        try {
            PesquisaDAO objusuarioDAO = new PesquisaDAO();

            DefaultTableModel model = (DefaultTableModel) tabelaUsuario.getModel();
            model.setNumRows(0);
            ArrayList<UsuarioDTO> lista = objusuarioDAO.PesquisarUsuario();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getNickname(),
                    lista.get(num).getNivel(),
                    lista.get(num).getNumeroFase()
                });
            }
        } catch (java.lang.Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Valores VIEW: " + erro);
        }
    }
